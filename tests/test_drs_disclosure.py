@pytest.mark.parametrize("fixture", sorted(_EXPECTED))
def test_extract_corpus_fixture(fixture: str) -> None:
    text = (_FIX / fixture).read_text()
    got = extract_drs_disclosure(text)
    expected = _EXPECTED[fixture]
