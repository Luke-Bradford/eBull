def test_record_holders_count_sentence_alone_is_not_a_disclosure() -> None:
    # The mandated Item 201(b) sentence with no split context must not
    # anchor an extraction (guard for the record-holders anchor variant).
    text = (
        "As of March 22, 2023, there were 197,058 record holders of our Class A "
        "Common Stock. Dividends were held by record holders of record."
    )
    assert extract_drs_disclosure(text) is None


def test_cross_side_sanity_rejects_inconsistent_pcts() -> None:
    text = (
        "approximately 10.0 million shares (50%) were held by registered holders "
        "with our transfer agent and approximately 90.0 million shares (50%) were "
        "held by Cede & Co."
    )
    assert extract_drs_disclosure(text) is None


# --- DB: refresh (frontier + fake provider) + read path -------------------------

_NEXT_IID = [918600]


def _seed_instrument(conn: psycopg.Connection[tuple], *, symbol: str, cik: str) -> int:
    _NEXT_IID[0] += 1
    iid = _NEXT_IID[0]
    with conn.cursor() as cur:
        cur.execute(
            "INSERT INTO instruments (instrument_id, symbol, company_name, currency, is_tradable) "
            "VALUES (%s, %s, %s, 'USD', TRUE)",
            (iid, symbol, f"{symbol} Inc."),
        )
        cur.execute(
            "INSERT INTO external_identifiers (instrument_id, provider, identifier_type, identifier_value, is_primary) "
            "VALUES (%s, 'sec', 'cik', %s, TRUE)",
            (iid, cik),
        )
    conn.commit()
    return iid


class _FakeProvider:
    def __init__(self, bodies: dict[str, str]) -> None:
        self.bodies = bodies
        self.fetched: list[str] = []

    def fetch_document_text(self, absolute_url: str) -> str | None:
        self.fetched.append(absolute_url)
        return self.bodies.get(absolute_url)


@pytest.mark.db
class TestDrsRefresh:
    def test_frontier_refresh_and_read(self, ebull_test_conn: psycopg.Connection[tuple], tmp_path: Path) -> None:
        iid = _seed_instrument(ebull_test_conn, symbol="DRSX", cik="0001326380")
        disclosing = (_FIX / "gme_10k_2026-03-24.txt").read_text()
        with ebull_test_conn.cursor() as cur:
            cur.execute(
                """
                INSERT INTO sec_filing_manifest (accession_number, cik, form, source, subject_type,
                    subject_id, instrument_id, filed_at, primary_document_url, ingest_status)
                VALUES ('drs-acc-1', '0001326380', '10-K', 'sec_10k', 'issuer', %(iid)s, %(iid)s,
                        '2026-03-24T12:00:00Z', 'https://x/one', 'parsed'),
                       ('drs-acc-0', '0001326380', '10-Q', 'sec_10q', 'issuer', %(iid)s, %(iid)s,
                        '2025-12-09T12:00:00Z', 'https://x/zero', 'parsed')
                """,
                {"iid": iid},
            )
        ebull_test_conn.commit()

        provider = _FakeProvider({"https://x/one": disclosing, "https://x/zero": "no split here"})
        result = refresh_drs_disclosures(ebull_test_conn, provider)
        assert result.filings_examined == 2
        assert result.filings_extracted == 1
        assert result.filings_no_disclosure == 1

        info = _read_drs(ebull_test_conn, iid, today=date(2026, 7, 23))
        assert info is not None
        assert info.registered_shares == Decimal("66200000.0")
        assert info.holders_of_record == 177522
        assert info.as_of_date == date(2026, 3, 18)

        # Staleness: >400 days after the as-of → suppressed.
        assert _read_drs(ebull_test_conn, iid, today=date(2027, 6, 1)) is None

        # Frontier: second run examines nothing (no filings newer than the
        # newest stored observation's filed_at).
        provider2 = _FakeProvider({})
        result2 = refresh_drs_disclosures(ebull_test_conn, provider2)
        assert result2.filings_examined == 0
        assert provider2.fetched == []
