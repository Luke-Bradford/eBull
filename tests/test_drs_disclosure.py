# --- normalize_filing_text ------------------------------------------------------


def test_normalizer_heals_ixbrl_word_fragmentation() -> None:
    # Inline tags mid-word must strip to '' (a space would fragment the
    # word — the "approxim ately" corpus landmine); block tags space-join.
    html = (
        '<div>approxim<span style="x">ately</span> 66.2 million '
        'sh<ix:nonNumeric name="y">ares</ix:nonNumeric></div><p>next</p>'
    )
    assert normalize_filing_text(html) == " approximately 66.2 million shares next "


def test_normalizer_maps_entities() -> None:
    assert normalize_filing_text("Cede &amp; Co&#160;(&#8220;DTC&#8221;)") == 'Cede & Co ("DTC")'


# --- _parse_quantity ------------------------------------------------------------


@pytest.mark.parametrize(
    ("num", "unit", "expected"),
    [
        ("382.4", "million", Decimal("382400000.0")),
        ("1.2", "billion", Decimal("1200000000.0")),
        ("529,547,465", None, Decimal("529547465")),
        # AMC's missing-"million" shape: bare small decimal scales ×1e6.
        ("527.5", None, Decimal("527500000.0")),
        # A real absolute count above the 10k floor stays literal.
        ("14,021", None, Decimal("14021")),
    ],
)
def test_parse_quantity(num: str, unit: str | None, expected: Decimal) -> None:
    assert _parse_quantity(num, unit) == expected


